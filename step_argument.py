# step argument in python is defined in the range() function of for loop

# syntax - for variable in range(start,end,step)
       #here step define how many step will overlapping for reading the next number


#printing odd number using step arguments

for i in range(1,10,2):
    print(i)

#printing even number using step arguments

for i in range(2,10,2):
    print(i)


# Reverse number print using step arguments

for i in range(10, 0, -1):
    print(i)